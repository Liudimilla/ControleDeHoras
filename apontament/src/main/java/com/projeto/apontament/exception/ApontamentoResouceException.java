package com.projeto.apontament.exception;

public class ApontamentoResouceException extends Exception{

	
	public ApontamentoResouceException() {
		super();
		
	}

	public ApontamentoResouceException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public ApontamentoResouceException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public ApontamentoResouceException(String message) {
		super(message);
		
	}

	public ApontamentoResouceException(Throwable cause) {
		super(cause);
		
	}

	private static final long serialVersionUID = -1087855609841903303L;
	
	
	
	
	

}
